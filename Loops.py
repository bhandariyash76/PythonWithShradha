#loops in python : there are two types of loops while and for loop

# while loop 

# while condition: 
#  code to run in loop

i = 1
while i<=5:
    print("hello")
    i += 1
    
# this is a simple exxample of while loop which will print "hello" 5 times

# for loop

for i in range(5):
    print("hello")